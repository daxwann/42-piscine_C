int	match(char *s1, char *s2)
{
	int outcome;

	if (*s1 == '\0' && *s2 == '\0')
		return (1);
	if (*s1 != '*' && *s1 == *s2)
		return (match(s1 + 1, s2 + 1));
	if (*s2 == '*')
	{
		if (*s1 == '\0')
			return (match(s1, s2 + 1));
		else
		{
			outcome = match(s1, s2 + 1);
			if (outcome == 1)
				return (match(s1, s2 + 1));
			else
				return (match(s1 + 1, s2));
		}
	}
	return (0);
}
