#include "ft_btree.h"

void	btree_apply_infix(t_btree *root, void (*applyf)(void *))
{
	if (root != NULL)
	{
		btree_apply_infix(root->left);
		applyf(root->item);
		btree_apply_infix(root->right);
	}
}
